package me.outspending.invoice.core.interfaces

/**
 * This interface is used to define a startable. This is used to start classes.
 *
 * @since 0.0.1
 * @author Outspending
 */
interface Startable {
    /**
     * This method is used to start classes.
     *
     * @since 0.0.1
     */
    fun start()
}