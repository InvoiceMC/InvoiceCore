package me.outspending.invoice.core.interfaces

/**
 * This interface is used to define a stopable. This is used to stop classes.
 *
 * @since 0.0.1
 * @author Outspending
 */
interface Stopable {
    /**
     * This method is used to stop classes.
     *
     * @since 0.0.1
     */
    fun stop()
}